

function showButton() {
    const textToShow = document.querySelector('#textToShow');
    const showButtonImg = document.querySelector('#showButtonImg');
    const hideButtonImg = document.querySelector('#hideButtonImg');
    textToShow.style.display = 'flex';
    showButtonImg.style.display = 'none';
    hideButtonImg.style.display = 'block';
}

function closeButton() {
    const textToShow = document.querySelector('#textToShow');
    const showButtonImg = document.querySelector('#showButtonImg');
    const hideButtonImg = document.querySelector('#hideButtonImg');
    textToShow.style.display = 'none';
    showButtonImg.style.display = 'block';
    hideButtonImg.style.display = 'none';
};

